# 2. Hypotenuse using Pythagoras
a, b = 3, 4
hyp = math.sqrt(a*a + b*b)
print("Hypotenuse:", hyp)

# 3. Convert degrees to radians
deg = 180
print("Radians:", math.radians(deg))
