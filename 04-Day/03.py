# Updating List Items

# UPDATING LIST ITEMS
# Lists are changeable (mutable), so we can modify values.

marks = [45, 67, 89]
print("Original marks:", marks)

# Updating values by index
marks[0] = 50
marks[2] = 90

print("Updated marks:", marks)

# Adding explanation
# marks[0] = 50 means:
# Go to index 0 and replace the value with 50


