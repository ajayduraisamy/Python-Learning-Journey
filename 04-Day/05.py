# LOOPING THROUGH LISTS
# We can use for loop to print each item from a list.

names = ["Ajay", "Kumar", "Rahul", "Mohan"]

print("Printing names one by one:")
for n in names:
    print(n)

# Explanation:
# for n in names:
#     print(n)
# Means:
# - Take each value from the list 'names'
# - Store it in variable n
# - Print it


